    private void btn_editActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_editActionPerformed
        // TODO add your handling code here:
        Connection con = null;

        try{
            con = getConection();
            ps = con.prepareStatement("SELECT * FROM producto WHERE id = ?");
            ps.setString(1, id_edit.getText());
            rs = ps.executeQuery();
            if(rs.next()){
                name_field_mod.setText(rs.getString("nombre"));
                cost_field_mod.setText(rs.getString("precio"));
                desc_field_mod.setText(rs.getString("descripcion"));
                cat_field_mod.setText(rs.getString("categoria"));
                ub_field_mod.setText(rs.getString("ubicacion"));
            } else {
                JOptionPane.showMessageDialog(null, "No existe el producto");
            }
        } catch(Exception e){
            System.err.println(e);
        }
    }//GEN-LAST:event_btn_editActionPerformed


    public void cargarTabla(String valor){
    String [] fila = {"id", "nombre", "precio","descripcion", "categoria", "ubicacion"};
    String [] columna = new String[6];
    
    String sql = "select * from producto where nombre LIKE '%"+valor+"%'";
    
    model = new DefaultTableModel(null, fila);
    Connection con = null;
         con = getConection();
    try{
        st =  con.createStatement();
        rs = st.executeQuery(sql);
        while(rs.next()){
             columna[0]= rs.getString("id");
             columna[1]= rs.getString("nombre");
             columna[2]= rs.getString("precio");
             columna[3]= rs.getString("descripcion");
             columna[4]= rs.getString("categoria");
             columna[5]= rs.getString("ubicacion");
             model.addRow(columna);
         }
         searchtable.setModel(model);
         searchtable1.setModel(model);
         searchtable3.setModel(model);
         
    }catch(SQLException ex) {
        JOptionPane.showMessageDialog(null, ex);
        }
    }
    
    
